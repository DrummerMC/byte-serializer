/**
 * Used to define the text encoding for strings
 */
export enum TextEncoding {
    
    /**
     * Use the ascii encoder
     */
    ASCII = 'ascii',
    
    /**
     * Use the utf8 encoder
     */
    UTF8 = 'utf8',
    
    /**
     *  Use the utf16le encoder
     */
    UTF16LE = 'utf16le',
    
    /**
     * Use the ucs2 encoder
     */
    UCS2 = 'ucs2',
    
    /**
     * Use the base64 encoder
     */
    BASE64 = 'base64',
    
    /**
     * Use the utf8-latin1 encoder
     */
    LATIN1 = 'latin1'
}